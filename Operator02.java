public class Operator02 {
    public static void main(String[] args){

        //논리 연산자
        boolean b1 = false;
        boolean b2 = true;

        System.out.println("b1&&b2 : " + (b1&&b2));
        System.out.println("b1||b2 :" + (b1||b2));
        System.out.println("!b1:" + !b1);
        System.out.println("!b2:" + !b2);
    }
}
