public class Operator01 {
    public static void main(String[] args) {
        //증감연산자
        int x = 10;
        System.out.println("++x : " + (++x));
        System.out.println("--x : " + (--x));
        System.out.println("x++ : " + (x++));
        System.out.println("x : " + x);
        System.out.println("x-- : " + (x--));
        System.out.println("x : " + x);
    }
}
